// Easy 2.3: Multiplying Two Numbers
// Create a function that takes two arguments, multiplies them together, and returns the result.

function multiply(a, b) {
  return a * b;
}

// Test cases
console.log(multiply(5, 3) === 15);   // logs true
console.log(multiply(5, 4) === 20);   // logs true
console.log(multiply(7, 3) === 21);   // logs true
console.log(multiply(50, 3) === 150); // logs true
console.log(multiply(5, 30) === 150); // logs true